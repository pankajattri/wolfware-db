CREATE TABLE Staff (
    staffID CHAR(64),
    storeID CHAR(64),
    name VARCHAR(128),
    age INTEGER,
    homeAddress VARCHAR(128),
    jobTitle VARCHAR(64),
    phoneNumber VARCHAR(16),
    emailAddress VARCHAR(64),
    timeOfEmployment TIMESTAMP
)

CREATE TABLE Supply (
	supplierID CHAR(64),
	transactionID CHAR(64)
)

CREATE TABLE CLUBMEMBERS (
	CUSTOMERID CHAR(10), 
	FIRSTNAME VARCHAR(100), 
	LASTNAME VARCHAR(100), 
	MEMBERSHIPLEVEL VARCHAR(50), 
	EMAILADDRESS VARCHAR(250), 
	PHONENO VARCHAR(15), 
	HOMEADDRESS VARCHAR(1000), 
	ACTIVESTATUS VARCHAR(20)
)

CREATE TABLE SIGNSUPCANCELS (
	STAFFID CHAR(7), 
	CUSTOMERID CHAR(10), 
	ACTIONDATE DATE,
	ACTION VARCHAR(8)
)

CREATE TABLE SUPPLIERS (
	SUPPLIERID CHAR(7), 
	SUPPLIERNAME CHAR(150), 
	EMAILADDRESS VARCHAR(250), 
	PHONE VARCHAR(15), 
	LOCATION  VARCHAR(1000)
)

CREATE TABLE HASSUPPLIERS (
	SUPPLIERID CHAR(7), 
	STOREID CHAR(7)
)
